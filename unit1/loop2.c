#include <stdio.h>

int main(void)
{
    // Example of while loop
    int i = 0;
    while (i < 3)
    {
        printf("HELLO \n");
        i++;
    }

    printf("\n\n");

    //Example of a for loop

    for(int j = 0; j < 3; j++)
    {
        printf("Hello\n");
    }

    printf("\n\n");

    //Example of a do while

    int k = 0;
    do
    {
        printf("heLLo!!\n");
        k++;
    }
    while (k < 3);
}